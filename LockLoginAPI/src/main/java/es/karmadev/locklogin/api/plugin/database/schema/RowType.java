package es.karmadev.locklogin.api.plugin.database.schema;

/**
 * Simple row types
 */
public enum RowType {
    /**
     * Varchar
     */
    VARCHAR,
    /**
     * Tiny integer
     */
    TINY_INTEGER,
    /**
     * Number
     */
    INTEGER,
    /**
     * Big integer
     */
    BIG_INTEGER,
    /**
     * Double value
     */
    DOUBLE,
    /**
     * Long value
     */
    LONG,
    /**
     * MySQL timestamp
     */
    TIMESTAMP,
    /**
     * Float value
     */
    FLOAT,
    /**
     * Any number
     */
    NUMERIC,
    /**
     * Text
     */
    TEXT,
    /**
     * A long text
     */
    LONGTEXT,
    /**
     * Blob
     */
    BLOB,
    /**
     * Boolean
     */
    BOOLEAN
}
